    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String course= (String)jComboBox1.getSelectedItem();
        String branch= (String)jComboBox2.getSelectedItem();
        String roll= rollno.getText();
        String name= name1.getText();
        String gender= (String)jComboBox3.getSelectedItem();
        String father= fathername.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/SRMS","root","cSR@1247485");
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branch,name,gender,father) values('"+roll+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+father+"')");
            JOptionPane.showMessageDialog(null,"Data Saved Succesfully");  
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

