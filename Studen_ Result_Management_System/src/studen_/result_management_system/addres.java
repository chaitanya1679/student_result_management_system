    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String OS= os.getText();
        String phys= phy.getText();
        String math= mat.getText();
        String DBMS= dbms.getText();
        String EM= em.getText();
        String rollno= roll.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/SRMS","root","cSR@1247485");
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,math,em,dbms,os) values('"+rollno+"','"+phys+"','"+math+"','"+EM+"','"+DBMS+"','"+OS+"')");
                JOptionPane.showMessageDialog(null,"Marks updated Succesfully");
                setVisible(false);
                new addres().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roll no not registered");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

